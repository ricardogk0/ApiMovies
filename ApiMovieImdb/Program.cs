using ApiMovieImdb.Interfaces;
using ApiMovieImdb.Mappings;
using ApiMovieImdb.Rest;
using ApiMovieImdb.Services;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.

builder.Services.AddControllers();
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();

builder.Services.AddSingleton<IMovieService, MovieService>();
builder.Services.AddSingleton<IOmdbApi, MovieRest>();

builder.Services.AddAutoMapper(typeof(MovieMapping));

var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.UseAuthorization();

app.MapControllers();

app.Run();
